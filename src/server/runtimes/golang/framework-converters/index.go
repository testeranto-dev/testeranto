// GinkgoConverter handles Ginkgo BDD test files
type GinkgoConverter struct{}

func (g *GinkgoConverter) Name() string {
	return "ginkgo"
}

func (g *GinkgoConverter) Detect(filePath string) bool {
	if !strings.HasSuffix(filePath, ".go") {
		return false
	}
	
	content, err := os.ReadFile(filePath)
	if err != nil {
		return false
	}
	
	contentStr := string(content)
	
	// Check for Ginkgo patterns
	hasGinkgoImport := strings.Contains(contentStr, "github.com/onsi/ginkgo") || 
	                  strings.Contains(contentStr, "github.com/onsi/ginkgo/v2")
	hasGinkgoDescribe := strings.Contains(contentStr, "Describe(") || 
	                     strings.Contains(contentStr, "It(") ||
	                     strings.Contains(contentStr, "BeforeEach(")
	
	return hasGinkgoImport && hasGinkgoDescribe
}

