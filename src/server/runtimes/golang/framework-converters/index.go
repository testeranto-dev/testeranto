// Framework converter interface for Go
package frameworkconverters

import (
	"os"
	"path/filepath"
	"strings"
)

// FrameworkConverter defines the interface for all framework converters
type FrameworkConverter interface {
	Name() string
	Detect(filePath string) bool
	GenerateWrapper(entryPointPath string, detectionResult map[string]interface{}, filesHash string) string
	TranslateToTesteranto(detectionResult map[string]interface{}) map[string]interface{}
}

// Registry of all framework converters
var Converters = []FrameworkConverter{
	&GoTestConverter{},
	&GinkgoConverter{},
	&TestifyConverter{},
	&GenericGoConverter{},
}

// DetectFramework detects which framework a Go test file uses
func DetectFramework(filePath string) FrameworkConverter {
	for _, converter := range Converters {
		if converter.Detect(filePath) {
			return converter
		}
	}
	return &GenericGoConverter{}
}

// GoTestConverter handles standard Go test files
type GoTestConverter struct{}

func (g *GoTestConverter) Name() string {
	return "gotest"
}

func (g *GoTestConverter) Detect(filePath string) bool {
	if !strings.HasSuffix(filePath, ".go") {
		return false
	}
	
	content, err := os.ReadFile(filePath)
	if err != nil {
		return false
	}
	
	contentStr := string(content)
	
	// Check for Go test patterns
	hasTestFunc := strings.Contains(contentStr, "func Test") && 
	              (strings.Contains(contentStr, "testing.T") || strings.Contains(contentStr, "*testing.T"))
	hasTestMain := strings.Contains(contentStr, "func TestMain")
	
	return hasTestFunc || hasTestMain
}

func (g *GoTestConverter) GenerateWrapper(entryPointPath string, detectionResult map[string]interface{}, filesHash string) string {
	absPath, _ := filepath.Abs(entryPointPath)
	
	return `// Go test wrapper generated by testeranto
// Hash: ` + filesHash + `
// Framework: gotest
// This file loads and executes Go tests

package main

import (
	"fmt"
	"os"
	"testing"
)

func main() {
	fmt.Println("Running Go test:", "` + absPath + `")
	
	// In Go, we can't dynamically import test files like in JavaScript
	// Instead, we need to run the tests using the go test command
	// This wrapper will be executed by the Go test runner
	
	// For now, we'll just print a message
	// The actual test execution will be handled by the native Go test runner
	fmt.Println("Test file detected. Use 'go test' to run tests.")
	
	// Exit with success code
	os.Exit(0)
}
`
}

